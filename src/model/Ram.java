package model;

public class Ram {
    private int idRam;
    private int nombreRam;

    public Ram(int idRam, int nombreRam) {
        this.idRam = idRam;
        this.nombreRam = nombreRam;
    }

    public int getIdRam() {
        return idRam;
    }

    public int getNombreRam() {
        return nombreRam;
    }
}
